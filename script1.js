function comenzar(){
	var elemento = document.getElementById("lienzo1");
	lienzo = elemento.getContext("2d");
	//indica cuando inicia con un trazo
	lienzo.beginPath();
	//mueve el trazo a una cordenada
	lienzo.moveTo(100,50);
	//crea linea
	lienzo.lineTo(100,50);
	lienzo.lineTo(50,150);

	//final de la linea
	lienzo.lineTo(150,150);
	//finalizar el trazo o cerrar dibujo
	lienzo.closePath();

	//crear mascara
	lienzo.clip();

	//lienzo.beginPath();
	//for(i=0;i<250;i+=10){
	//	lienzo.moveTo(0,i);
	//	lienzo.lineTo(500,i);
	//}

	//lienzo.stroke();
	lienzo.fill();


}
window.addEventListener("load",comenzar,false);

